# 16-, 8-, 2-ые системы счисления

print(64, 0o100, 0x40, 0b1000000)  # 64 64 64 64
print(int('64'), int('100', 8), int('40', 16), int('1000000', 2))  # 64 64 64 64
print(int('0x40', 16), int('0b1000000', 2))  # литеральные формы также поддерживаются

print(eval('64'), eval('0o100'), eval('0x40'), eval('0b1000000'))  # 64 64 64 64

print('{0:o}, {1:x}, {2:b}'.format(64, 64, 64))  # 100, 40, 1000000
print('%o, %x, %x, %X' % (64, 64, 255, 255))  # 100, 40, ff, FF
