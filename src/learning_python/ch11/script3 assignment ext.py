# Расширенная распаковка последовательности
seq = [1, 2, 3, 4]
a, *b = seq
print(a)
# 1
print(b)
# [2, 3, 4]

*a, b = seq
print(a)
# [1, 2, 3]
print(b)
# 4

a, *b, c = seq
print(a)
# 1
print(b)
# [2, 3]
print(c)
# 4

# Итерируемый объект
a, *b, c = range(4)
print(a, b, c)
# 0 [1, 2] 3

# Пример с циклом
L = [1, 2, 3, 4]
while L:
    front, *L = L  # Получение первого и остальных элементов без нарезания
    print(front, L)
# 1 [2, 3, 4]
# 2 [3, 4]
# 3 [4]
# 4 []
